public class Personne {
    private String nom, fonction;

    public Personne(String nom, String fonction) {
        this.nom = nom;
        this.fonction = fonction;
    }

    public String getNom() {
        return nom;
    }

    public String getFonction() {
        return fonction;
    }


}
